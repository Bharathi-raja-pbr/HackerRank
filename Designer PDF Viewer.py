def designerPdfViewer(h, word):
    # Write your code here
    arr=[]
    word=word.upper()
    
    for x in word:
        arr.append(ord(x)%65)
    # print(arr)
    val=[]
    for x in arr:
        val.append(h[x])
    #print(val)
    return len(word)*max(val)

